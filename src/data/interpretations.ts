// SECTION 4: 역량 32개 해설 (4분기: 점수x나다움)
// 나다움 2단계: 나다움 / 非나다움
// ============================================================

export function getCompetencyInterpretation(compName: string, score: number, nadaumLevel: NadaumLevel): string {
  const isNadaum = nadaumLevel === 'nadaum';

  // 4가지 분기: score high/low x nadaum/non-nadaum
  if (score >= 60 && isNadaum) {
    return `${compName}은 나다운 역량이면서 실제로도 높은 수준. 이 역량이 핵심인 직무에서 에너지 소모 없이 오래 일할 수 있습니다.`;
  }
  if (score >= 60 && !isNadaum) {
    return `${compName}은 나다운 영역은 아니지만 높은 수준으로 키웠습니다. 잘하지만 에너지가 더 들 수 있으니 컨디션 관리에 신경 쓰세요.`;
  }
  if (score < 60 && isNadaum) {
    return `${compName}은 나다운 역량인데 아직 충분히 발현되지 않았습니다. 경험이 쌓이면 빠르게 성장할 수 있는 잠재력이 있습니다.`;
  }
  // score < 60 && non-nadaum
  return `${compName}은 의식적 노력이 필요한 영역입니다. Core Fit 직무에 필요한 경우에만 집중 개발하세요.`;
}


