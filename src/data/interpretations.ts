export function generateSummary(
  focusType: FocusType,
  focusLevel: FocusLevel,
  anchorTop1: AnchorKey,
  anchorTop2: AnchorKey,
  energyLevel: EnergyLevel,
  capacityLevel: CapacityLevel,
  coreJob: string,
  orgType: string,
): SummaryResult {
  const anchorKey1 = [anchorTop1, anchorTop2].sort().join('_');
  const anchorKey2 = `${anchorTop1}_${anchorTop2}`;
  const anchorKey3 = `${anchorTop2}_${anchorTop1}`;
  const anchorPhrase = SUMMARY_ANCHOR[anchorKey1] || SUMMARY_ANCHOR[anchorKey2] || SUMMARY_ANCHOR[anchorKey3] || '자신만의 가치를 추구하는';

  const focusPhrase = SUMMARY_FOCUS[`${focusType}_${focusLevel}`] || '자신만의 방식으로 일하는';

  const line1 = `${focusPhrase} 당신은,`;
  const line2 = `${anchorPhrase} 사람입니다.`;
  const line3 = `${SUMMARY_ENERGY[energyLevel] || ''} ${orgType}의 ${coreJob}${SUMMARY_CAPACITY[capacityLevel] || ''}`;

  return { line1, line2, line3, full: `${line1}\n${line2}\n${line3}` };
}
